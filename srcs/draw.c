#include "RTv1.h"

void    put_pixel(float x, float y, t_raytrace *r, int col)
{
    if (x >= 0 && y >= 0 && x <= r->w && y <= r->h)
        *(unsigned int *)(r->data + ((int)x * r->bpp) + ((int)y * r->sl)) = col;
}