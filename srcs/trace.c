#include "RTv1.h"

void	trace(t_raytrace *r)
{
	double	x;
	double	y;

	x = 0;
	while (x < r->w)
	{
		y = 0;
		while (y < r->h)
		{
			put_pixel(x, y, 0xFF00A3, r);
			y += 0.1f;
		}
		x += 0.1f;
	}
}